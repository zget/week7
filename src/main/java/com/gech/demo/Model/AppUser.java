package com.gech.demo.Model;

import org.springframework.context.annotation.Bean;
import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import org.springframework.security.crypto.password.PasswordEncoder;

import javax.persistence.*;
import java.util.HashSet;
import java.util.Set;

@Entity
public class AppUser {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private long id;

    private String username;

    private String password;

    private String displayName;

    @ManyToMany(fetch = FetchType.EAGER)
    Set<AppRole> roles;

    @ManyToMany()
    Set<UserProfile> choices;


    @Transient //Equivalent to an ignore statement
    private PasswordEncoder encoder;


    @Transient //Equivalent to an ignore statement
    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }

    public AppUser() {
        this.roles = new HashSet<>();
        this.choices=new HashSet<>();
        encoder = passwordEncoder();
    }

    public AppUser(String username, String password, AppRole role) {
        this.username = username;
        this.roles = new HashSet<>();
        this.choices=new HashSet<>();
        addRole(role);
        encoder = passwordEncoder();
        setPassword(password);

    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = encoder.encode(password);
        System.out.println("Password:"+this.password);
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public Set<AppRole> getRoles() {
        return roles;
    }

    public void setRoles(Set<AppRole> roles) {
        this.roles = roles;
    }

    public Set<UserProfile> getChoices() {
        return choices;
    }

    public void setChoices(Set<UserProfile> choices) {
        this.choices = choices;
    }

    public void addRole(AppRole r)
    {
        this.roles.add(r);
    }

    public void addNews(UserProfile p)
    {
        this.choices.add(p);
    }
}
